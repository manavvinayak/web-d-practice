// Function with parameters in JavaScript
function greet(name, age) {
    console.log(`Hello, my name is ${name} and I am ${age} years old.`);
}

greet('Alice', 30);
greet('Piyush', 22);
greet('Vinayak', 21);

function hello(name) {
    console.log(`Hey, ${name}! How are you doing?`);
}
// Calling the function with arguments
hello('Kirit');
hello('Piyush')
hello('Vinayak')


